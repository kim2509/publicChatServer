package com.dy.domain;

public class UserProfileKeywordDic {

	private int keywordNo;
	
	private String keywordCategory;
	
	private String keywordName;
	
	private int keywordOrder;

	public int getKeywordNo() {
		return keywordNo;
	}

	public void setKeywordNo(int keywordNo) {
		this.keywordNo = keywordNo;
	}

	public String getKeywordCategory() {
		return keywordCategory;
	}

	public void setKeywordCategory(String keywordCategory) {
		this.keywordCategory = keywordCategory;
	}

	public String getKeywordName() {
		return keywordName;
	}

	public void setKeywordName(String keywordName) {
		this.keywordName = keywordName;
	}

	public int getKeywordOrder() {
		return keywordOrder;
	}

	public void setKeywordOrder(int keywordOrder) {
		this.keywordOrder = keywordOrder;
	}
}
